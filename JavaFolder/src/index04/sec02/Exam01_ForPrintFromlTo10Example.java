package index04.sec02;

public class Exam01_ForPrintFromlTo10Example {

	public static void main(String[] args) {
		/*for문: 중문안의 구문을 조건에 맞춰 반복
		 * 초기화(초기문) 실행 횟수: 1번
		 * 구문 종료 조건: 조건부*/
		
		//1~10까지 출력
		for(int i=1;/*←초기문*/ i<=10;/*←조건부*/ i++) {
			System.out.println(i);
		}

	}

}


